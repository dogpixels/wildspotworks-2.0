    <h1>Häufig gestellte Fragen</h1>

    <h2>F: Wieviel kostet so ein Kostüm und wie setzt sich der Preis zusammen?</h2>
    <p>A: Der Preis errechnet sich aus Materialkosten und Arbeitszeit. Je aufwändiger ein Fursuit ist desto mehr wird es am Ende kosten. Du kannst dir einen ungefähren Eindruck auf der  <a href="de/prices">Preise- Seite</a> machen.</p>

    <h2>F: Wie bestelle Ich?</h2>
    <p>A: Wenn ich einen Slot für Commissions geöffnet habe (Was für gewöhnlich auf der <a href="de/home">Startseite</a> angezeigt wird), klicke dich einfach durch den<a href="de/prices">Preisrechner</a>und sende mir das Kontaktformular Ich werde alsbald möglich antworten. Ich nehme mir jedoch das recht herraus auch Anfragen abzulehnen.</p>
    
    <h2>F: Wann öffnest du für Comissions?</h2>
    <p>A: Für gewöhnlich immer dann wenn ich fertig mit einem Projekt bin und am Beginn eines Monats- Verfolg einfach meinen <a href="https://twitter.com/LunoWroo">Twitter Feed</a> oder <a href="http://www.furaffinity.net/user/lunowroo/">FurAffinity</a>, denn dort werde ich es vorher ankündigen.</p>
    

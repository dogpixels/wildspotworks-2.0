    <h2>F: Wie kann ich bezahlen?</h2>
    <p>A: Bei Bestellungen aus Deutschland würde ich Überweisung vorziehen. In jedem Fall kann aber auch mit PayPal bezahlt werden.</p>
    
    <h2>F: Was für Materialien benutzt du?</h2>
    <p>A: Für sämtliche Fursuits benutze ich sogenanntes "Faux Fur" oder auch "Luxury Shag". Der 3D-Druck wird mit pulverförmigem Nylon mittels SLS-Druck gefertigt.</p>
    
    <h2>F: Warum das aufwendige und teure Druckverfahren für die Köpfe?</h2>
    <p>A: Einfach, weil ich's kann! Und weil es einige Vorteile bietet: Ich kann sehr genau an der Kundenvorgabe arbeiten und sogar eine kleine Vorschau rendern. Außerdem ist die Kopfbasis sehr leicht, hat eine sehr gute Akustik und wird nicht ganz so heiß wie eine Resin- oder eine Schaumstoffbasis.</p>
    
    <h2>F: Mein Fursuit ist schmutzig geworden - wie wasche ich ihn?</h2>
    <p>A: Hierzu habe ich eine kleine <a href="de/care">Pflegeanleitung</a> gemacht. Wirf einfach einen Blick darauf.</p>
    
    <h2>F: An meinem Suit ist etwas kaputt - wie kann ich es reparieren?</h2>
    <p>A: Die gute Nachricht zuerst: Der komplette Suit ist handgemacht und im Grunde gibt es nichts daran, was man nicht reparieren kann. In einem gewissen Rahmen gilt natürlich auch die gesetzliche Gewährleistung für Fursuits, die ich hergestellt habe. In jedem Fall kannst du mich einfach <a href="de/contact">kontaktieren</a> und wir finden eine Lösung.</p>

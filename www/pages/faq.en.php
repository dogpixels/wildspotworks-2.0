    <h1>Frequently Asked Questions</h1>

    <h2>Q: How much does a costume cost and how do you calculate the price?</h2>
    <p>A: The price comes together from material and working hours; the more effort I put into it, the more expensive it'll get. You'll get a rough idea on my <a href="en/prices">pricing page</a>.</p>

    <h2>Q: How can I order?</h2>
    <p>A: If I am open for comissions (that's usually advertised on the <a href="en/home">front page</a>, but you can always ask), feel free to fill out the the <a href="en/prices">price calculator</a> and submit it to me. I'll get back at you as soon as possible. Please note that I reserve the right to decline requests.</p>
	
	<h2>Q: When are you open for comissions?</h2>
    <p>A: Usually as soon as I have finished my current projects - I will announce the dates on my <a href="https://twitter.com/LunoWroo">Twitter account</a> and on <a href="http://www.furaffinity.net/user/lunowroo/">FurAffinity</a>.</p>
	
    <img src="img/pages/faq/faqluno.png" alt="" class="uk-float-right uk-visible@s" />
	<h2>Q: What sort of payment do you accept?</h2>
    <p>A: If you order from Germany, I'd prefer bank transfer. Otherwise you can pay via PayPal.</p>
	
	<h2>Q: What kind of materials are you using?</h2>
    <p>A: For the costumes I use high-quality faux fur and a 3D-printed nylon head base.</p>
	
	<h2>Q: Why do you use expansive 3D Printing for the head?</h2>
    <p>A: Simply because of its' advantages: I can design the head base according to the customer's desire and even render a small 3D preview of how the suit will look like. Furthermore, the base is very light, provides good accoustic properties and won't heat you up as much as a foam or resin base.</p>
	
	<h2>Q: My Costume got dirty - How can I wash it?</h2>
    <p>A: Please see the <a href="en/care">information page "Care Guide"</a> about what you can and shouldn't do about it.</p>
	
	<h2>Q: Something is broken, how do I get it fixed?</h2>
    <p>A: Since the whole suit is handmade there's nothing one cannot repair. I'll fix things to a certain extend, depending on the nature of the issue. Please <a href="en/contact">contact</a> me on any way to work something out.</p>

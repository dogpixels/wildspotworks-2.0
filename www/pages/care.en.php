    <h3>The Next Con Seems Years Away - How to Store the Suit</h3>
    <p>
        It may be nice to put your fursuit's head on the cupboard for display; however, that's not a good idea. The fur is our culprit once again: it provides a huge surface for any kind of dust, and leaving it out will make it catch dust like crazy. If you want to enjoy your suit for a long time, put it into a plastic bag in a dark box where it is protected from dust and sunlight. Make sure you store it without any kinks. As I said before, the fur tends to memorize this, especially over time.
    </p>

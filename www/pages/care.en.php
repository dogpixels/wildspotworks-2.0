    <h1>How to Take Care of Your Fursuit</h1>
    
    <h3>Before You Start Suiting...</h3>
    <p>
        A fursuit is an exceptional type of clothing. On the one hand, it is a performance costume that has to provide good properties for acting, while on the other hand, it's made out of faux fur, which works against that. The fur will make you sweat while you wear it, it will catch dust over time, and the hairs will get kinks.<br>
        All of that is inevitable - but with good care, you can enjoy your suit for a much longer time.<br>
        Use your common sense, and treat it like you would treat a classic suit.
    </p>

    <img src="img/pages/care/infoluno.png" alt="" class="uk-float-right uk-hidden@s" />

    <h3>Put Your Suit On!</h3>
    <p>
        It may sound like a small detail, but take your time putting on your suit and don't hurry. Most incidents happen when you're in a rush.<br>
        It is advisable to have a second pair of hands when putting on your suit.<br>
        An often underestimated part of a performance is the clothing under your suit. Wearing HeatGear or similar underwear can reduce discomfort from heat and protect the costume from being soaked in sweat.<br>
    </p>

    <h3>The Performance is Over - What Now?</h3>
    <p>
        Take any brush you would use for your own hair and use it on your fursuit. The fur fibers tend to "memorize" kinks, but if you brush it regularly, you can reduce this effect. It's also recommended to turn your suit inside out, hang it somewhere to dry, and spray it with disinfectant spray.
    </p>

    <h3>Washing Your Suit is Inevitable</h3>
    <p>
        The fursuit body is usually machine washable - turn your suit inside out, use a cold program <b>(e.g., not above 30&#xba; C)</b> and use a delicate cycle.<br>
        <b>Do not put it into a dryer</b>, however! The dryer is too hot and will ruin the fur permanently.<br>
        It is recommended to wash all other parts of the suit by hand.<br>
